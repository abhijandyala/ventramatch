def _fmt_ms(v: Any, d: int = 4) -> str:
    """Format a float for the multiseed report; returns '—' for None/NaN."""
    if v is None:
        return "—"
    if isinstance(v, float) and v != v:
        return "—"
    try:
        return f"{float(v):.{d}f}"
    except (TypeError, ValueError):
        return str(v)


