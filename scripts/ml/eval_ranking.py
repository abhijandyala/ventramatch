def multiseed_main() -> None:
    """Evaluate ranking metrics across multiple seeds using predictions_eligible_oof_multiseed.csv."""
    ms_path = ARTIFACTS_DIR / "predictions_eligible_oof_multiseed.csv"
    if not ms_path.exists():
        print("ERROR: predictions_eligible_oof_multiseed.csv not found.")
        print("Run:  npm run train-multiseed:synthetic-matches")
        sys.exit(1)

    ARTIFACTS_DIR.mkdir(parents=True, exist_ok=True)
    print("\n" + "═" * 72)
    print("  MULTI-SEED RANKING EVALUATION — PHASE 11c-i")
    print("═" * 72)
    print("  ⚠  All data is SYNTHETIC. Not investment advice.")
    print("═" * 72 + "\n")

    print(f"Loading {ms_path.name} …")
    ms_df = pd.read_csv(ms_path)

    # Load features for baselines.
    with PAIRS_PATH.open(encoding="utf-8") as fh:
        raw_pairs: list[dict[str, Any]] = json.load(fh)
    feat_rows: list[dict[str, Any]] = []
    for p in raw_pairs:
        row: dict[str, Any] = {"startup_id": p["startup_id"], "investor_id": p["investor_id"]}
        for k, v in p["features"].items():
            row[k] = float(v) if v is not None else 0.0
        feat_rows.append(row)
    feat_df = pd.DataFrame(feat_rows)

    seeds = sorted(ms_df["seed"].unique().tolist())
    model_names = sorted(ms_df["model_name"].unique().tolist())
    pairs_per = (
        len(ms_df[(ms_df["seed"] == seeds[0]) & (ms_df["model_name"] == model_names[0])])
        if seeds and model_names else 0
    )
    n_founders = (
        ms_df[(ms_df["seed"] == seeds[0]) & (ms_df["model_name"] == model_names[0])][
            "startup_id"
        ].nunique()
        if seeds and model_names else 0
    )

    print(f"  Seeds: {seeds}")
    print(f"  Models: {model_names}")
    print(f"  Pairs per (seed × model): {pairs_per}  |  Unique founders: {n_founders}\n")

    # Per-seed aggregate storage: model_name → list of per-seed agg dicts.
    pf_seed_aggs: dict[str, list[dict[str, Any]]] = {m: [] for m in model_names}
    pi_seed_aggs: dict[str, list[dict[str, Any]]] = {m: [] for m in model_names}

    for seed in seeds:
        seed_data = ms_df[ms_df["seed"] == seed].copy()
        for model_name in model_names:
            model_data = seed_data[seed_data["model_name"] == model_name].copy()
            model_data = model_data.merge(
                feat_df, on=["startup_id", "investor_id"], how="left"
            )
            model_data["score_scorematch_approx"] = scorematch_approx_scores(model_data)
            model_data["score_random"] = random_scores(len(model_data), random_state=seed)
            pf_seed_aggs[model_name].append(
                aggregate_strategy(evaluate_strategy(model_data, "expected_label", "startup_id"))
            )
            pi_seed_aggs[model_name].append(
                aggregate_strategy(evaluate_strategy(model_data, "expected_label", "investor_id"))
            )

    # Baselines: compute once with first-seed, first-model data.
    base = ms_df[
        (ms_df["seed"] == seeds[0]) & (ms_df["model_name"] == model_names[0])
    ].copy().merge(feat_df, on=["startup_id", "investor_id"], how="left")
    base["score_scorematch_approx"] = scorematch_approx_scores(base)
    base["score_random"] = random_scores(len(base), random_state=42)
    pf_sm = aggregate_strategy(evaluate_strategy(base, "score_scorematch_approx", "startup_id"))
    pf_rnd = aggregate_strategy(evaluate_strategy(base, "score_random", "startup_id"))
    pi_sm = aggregate_strategy(evaluate_strategy(base, "score_scorematch_approx", "investor_id"))

    # Aggregate across seeds.
    KEY_METRICS = [
        "mean_ndcg_at_3", "mean_ndcg_at_5", "mean_ndcg_at_10",
        "mean_precision_at_5", "mean_recall_at_5", "mean_ap_at_5",
        "overall_mean_rank_relevant",
    ]

    def _agg_seeds(agg_list: list[dict[str, Any]], key: str) -> dict[str, Any]:
        vals = [a.get(key, float("nan")) for a in agg_list]
        valid = [v for v in vals if v is not None and isinstance(v, (int, float)) and v == v]
        if not valid:
            return {"mean": float("nan"), "std": float("nan"),
                    "ci95": [float("nan"), float("nan")], "per_seed": vals}
        mean = float(np.mean(valid))
        std = float(np.std(valid))
        ci = 1.96 * std / max(1.0, len(valid) ** 0.5)
        return {
            "mean": round(mean, 4),
            "std": round(std, 4),
            "ci95": [round(mean - ci, 4), round(mean + ci, 4)],
            "per_seed": [round(v, 4) if (v is not None and v == v) else None for v in vals],
        }

    agg_pf = {m: {k: _agg_seeds(pf_seed_aggs[m], k) for k in KEY_METRICS} for m in model_names}
    agg_pi = {m: {k: _agg_seeds(pi_seed_aggs[m], k) for k in KEY_METRICS} for m in model_names}

    # Determine best LogReg variant by per-founder NDCG@5.
    logreg_names = [m for m in model_names if m.startswith("logreg")]
    ndcg5_means = {m: agg_pf[m]["mean_ndcg_at_5"]["mean"] for m in model_names}

    # Safety filter from multiseed_metrics.json.
    ms_metrics_path = ARTIFACTS_DIR / "multiseed_metrics.json"
    safe_logreg = logreg_names
    if ms_metrics_path.exists():
        ms_data = json.loads(ms_metrics_path.read_text(encoding="utf-8"))
        safety = ms_data.get("safety_diagnostic", {})
        safe_logreg = [m for m in logreg_names if safety.get(m, {}).get("passes_safety_floor", True)]
        if not safe_logreg:
            safe_logreg = ["logreg_c1.0"]

    best_logreg = max(safe_logreg, key=lambda m: ndcg5_means.get(m, -1.0))
    best_c = float(best_logreg.replace("logreg_c", ""))
    best_ndcg5 = ndcg5_means.get(best_logreg, float("nan"))
    best_ci = agg_pf.get(best_logreg, {}).get("mean_ndcg_at_5", {}).get("ci95", [None, None])

    gbm_ci_upper = agg_pf.get("gbm", {}).get("mean_ndcg_at_5", {}).get("ci95", [None, None])[1]
    logreg_ci_above_gbm: "bool | None" = None
    if best_ci[0] is not None and gbm_ci_upper is not None:
        logreg_ci_above_gbm = best_ci[0] > gbm_ci_upper

    sm_ndcg5 = pf_sm.get("mean_ndcg_at_5", float("nan"))
    rnd_ndcg5 = pf_rnd.get("mean_ndcg_at_5", float("nan"))

    # Print summary.
    print(f"{'─' * 72}")
    print("PER-FOUNDER NDCG@5  (mean ± CI95 across seeds)")
    print(f"{'─' * 72}")
    for m in sorted(model_names, key=lambda x: ndcg5_means.get(x, -1), reverse=True):
        ci = agg_pf[m]["mean_ndcg_at_5"]["ci95"]
        print(
            f"  {m:<26}  {ndcg5_means[m]:.4f}  "
            f"±{agg_pf[m]['mean_ndcg_at_5']['std']:.4f}  "
            f"CI95=[{ci[0]:.4f},{ci[1]:.4f}]"
        )
    print(f"  {'scorematch_approx':<26}  {sm_ndcg5:.4f}  (reference, seed=42)")
    print(f"  {'random':<26}  {rnd_ndcg5:.4f}  (reference, seed=42)")
    print()
    print(f"  Best LogReg (ranking): {best_logreg}  C={best_c}")
    if logreg_ci_above_gbm is True:
        print(f"  ✓  LogReg CI lower ({best_ci[0]:.4f}) > GBM CI upper ({gbm_ci_upper:.4f})")
    elif logreg_ci_above_gbm is False:
        print("  ℹ  LogReg and GBM CIs overlap (not statistically distinguishable at 95%)")
    print()

    # Save JSON.
    metrics_out: dict[str, Any] = {
        "generated": datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"),
        "seeds": seeds,
        "n_eligible_pairs": pairs_per,
        "n_founders": n_founders,
        "k_values": K_VALUES,
        "model_names": model_names,
        "per_founder": agg_pf,
        "per_founder_baselines": {
            "scorematch_approx": {k: pf_sm.get(k) for k in KEY_METRICS},
            "random_seed42": {k: pf_rnd.get(k) for k in KEY_METRICS},
        },
        "per_investor": agg_pi,
        "per_investor_baselines": {
            "scorematch_approx": {k: pi_sm.get(k) for k in KEY_METRICS},
        },
        "ranking_based_selection": {
            "best_model": best_logreg,
            "best_c_value": best_c,
            "best_ndcg5_mean": round(best_ndcg5, 4) if best_ndcg5 == best_ndcg5 else None,
            "best_ndcg5_ci95": best_ci,
            "vs_random_lift_ndcg5": (
                round(best_ndcg5 - rnd_ndcg5, 4)
                if best_ndcg5 == best_ndcg5 and rnd_ndcg5 == rnd_ndcg5 else None
            ),
            "vs_scorematch_lift_ndcg5": (
                round(best_ndcg5 - sm_ndcg5, 4)
                if best_ndcg5 == best_ndcg5 and sm_ndcg5 == sm_ndcg5 else None
            ),
            "logreg_ci_strictly_above_gbm_ci": logreg_ci_above_gbm,
        },
    }

    metrics_path = ARTIFACTS_DIR / "ranking_metrics_multiseed.json"
    metrics_path.write_text(json.dumps(metrics_out, indent=2), encoding="utf-8")
    print(f"  ✓ {metrics_path.name}")

    report_path = write_multiseed_ranking_report(metrics_out, model_names, seeds, ARTIFACTS_DIR)
    print(f"  ✓ {report_path.name}")

    print(f"\n{'═' * 72}")
    print("  Done. Multi-seed ranking artifacts saved.")
    print("  ⚠  Synthetic experimental only. Not for production use.")
    print("═" * 72 + "\n")


