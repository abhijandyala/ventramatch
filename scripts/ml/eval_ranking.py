# ── Ranking metric functions ──────────────────────────────────────────────────


def ndcg_at_k(labels: list[int], k: int) -> float:
    """NDCG@K using graded relevance (raw label as gain: gain = 2^label - 1).

    Returns 1.0 when all labels are 0 (trivially perfect — nothing to mis-rank).
    Returns NaN when k < 1 or the list is empty.
    """
    if not labels or k < 1:
        return float("nan")
    k_eff = min(k, len(labels))
    arr = np.array(labels[:k_eff], dtype=float)
    discounts = np.log2(np.arange(2, k_eff + 2, dtype=float))
    dcg = float(np.sum((2.0 ** arr - 1.0) / discounts))

    ideal = sorted(labels, reverse=True)[:k_eff]
    ideal_arr = np.array(ideal, dtype=float)
    idcg = float(np.sum((2.0 ** ideal_arr - 1.0) / discounts))

    if idcg == 0.0:
        return 1.0  # all labels are 0; ranking is trivially optimal
    return float(np.clip(dcg / idcg, 0.0, 1.0))


def precision_at_k(labels: list[int], k: int,
                   threshold: int = RELEVANT_THRESHOLD) -> float:
    """Fraction of top-K items with label >= threshold."""
    if not labels or k < 1:
        return float("nan")
    k_eff = min(k, len(labels))
    return sum(1 for l in labels[:k_eff] if l >= threshold) / k_eff


def recall_at_k(labels: list[int], k: int,
                threshold: int = RELEVANT_THRESHOLD) -> float:
    """Fraction of all relevant items retrieved in the top-K positions."""
    n_relevant = sum(1 for l in labels if l >= threshold)
    if n_relevant == 0:
        return float("nan")
    k_eff = min(k, len(labels))
    return sum(1 for l in labels[:k_eff] if l >= threshold) / n_relevant


def ap_at_k(labels: list[int], k: int,
            threshold: int = RELEVANT_THRESHOLD) -> float:
    """Average Precision@K.

    Denominator is total relevant items in the full list (not capped at K),
    following the TREC convention.
    """
    n_rel_total = sum(1 for l in labels if l >= threshold)
    if n_rel_total == 0:
        return float("nan")
    k_eff = min(k, len(labels))
    hits = 0
    ap = 0.0
    for i in range(k_eff):
        if labels[i] >= threshold:
            hits += 1
            ap += hits / (i + 1)
    return ap / n_rel_total


def mean_rank_of_relevant(labels: list[int],
                          threshold: int = RELEVANT_THRESHOLD) -> float:
    """Mean 1-indexed rank of items with label >= threshold.

    Returns NaN when no relevant item exists in the list.
    Lower is better (rank 1 = top of list).
    """
    ranks = [i + 1 for i, l in enumerate(labels) if l >= threshold]
    return float("nan") if not ranks else float(np.mean(ranks))


# ── Group-level evaluation ─────────────────────────────────────────────────────


def eval_group(sorted_labels: list[int], k_values: list[int] = K_VALUES) -> dict[str, Any]:
    """Compute all metrics for a single ranked list."""
    row: dict[str, Any] = {
        "n_candidates": len(sorted_labels),
        "n_relevant": sum(1 for l in sorted_labels if l >= RELEVANT_THRESHOLD),
        "n_label4": sum(1 for l in sorted_labels if l == 4),
        "mean_rank_relevant": mean_rank_of_relevant(sorted_labels, RELEVANT_THRESHOLD),
        "mean_rank_label4":   mean_rank_of_relevant(sorted_labels, threshold=4),
    }
    for k in k_values:
        row[f"ndcg_at_{k}"]      = ndcg_at_k(sorted_labels, k)
        row[f"precision_at_{k}"] = precision_at_k(sorted_labels, k)
        row[f"recall_at_{k}"]    = recall_at_k(sorted_labels, k)
        row[f"ap_at_{k}"]        = ap_at_k(sorted_labels, k)
    return row


def evaluate_strategy(
    df: "pd.DataFrame",
    score_col: str,
    group_col: str,
    k_values: list[int] = K_VALUES,
) -> "pd.DataFrame":
    """Evaluate one ranking strategy across all groups defined by group_col.

    For each group (founder or investor), sort candidates by score_col descending
    and compute ranking metrics.

    Returns a DataFrame with one row per group and one column per metric.
    """
    rows: list[dict[str, Any]] = []
    for group_id, grp in df.groupby(group_col):
        sorted_grp = grp.sort_values(score_col, ascending=False)
        labels = sorted_grp["true_label"].tolist()
        m = eval_group(labels, k_values)
        m[group_col] = group_id
        rows.append(m)
    return pd.DataFrame(rows) if rows else pd.DataFrame()


# ── Data loading ───────────────────────────────────────────────────────────────


def load_data(source: str = "auto") -> "tuple[pd.DataFrame, str]":
    """Load eligible pairs with predictions and feature columns.

    Args:
        source: "auto" — use best available in-sample predictions (Phase 11a default).
                "oof"  — use out-of-fold predictions (Phase 11b, leakage-free).

    Preference order for "auto":
      1. predictions_eligible_all.csv  — 260 eligible pairs with split annotation.
      2. predictions_all.csv           — 595 pairs filtered to eligible.
      3. predictions.csv               — 52 test-set pairs only (warns).

    Returns (df_eligible, pred_source_description).
    """
    # ── Find predictions file ─────────────────────────────────────────────────
    if source == "oof":
        oof_path = ARTIFACTS_DIR / "predictions_eligible_oof.csv"
        if not oof_path.exists():
            print("ERROR: predictions_eligible_oof.csv not found.")
            print("Run:  npm run train:synthetic-matches  (eligible-only mode) first.")
            sys.exit(1)
        candidates = [(oof_path, "OOF predictions — leakage-free (Phase 11b)")]
    else:
        candidates = [
            (ARTIFACTS_DIR / "predictions_eligible_all.csv",
             "all eligible pairs — train+test (Phase 11a)"),
            (ARTIFACTS_DIR / "predictions_all.csv",
             "all 595 pairs — eligible-train-full-eval mode"),
            (ARTIFACTS_DIR / "predictions.csv",
             "test-set only (52 pairs) — fallback; ranking will be unreliable"),
        ]

    pred_path: "Path | None" = None
    pred_desc = ""
    for path, desc in candidates:
        if path.exists():
            pred_path = path
            pred_desc = desc
            break

    if pred_path is None:
        print("ERROR: No predictions file found in", ARTIFACTS_DIR)
        print("Run:  npm run train:synthetic-matches")
        sys.exit(1)

    print(f"  Predictions : {pred_path.name}  ({pred_desc})")
    if "test-set only" in pred_desc:
        print(
            "  ⚠  Only test-set predictions available (52 pairs). "
            "Per-founder ranking lists will be very short and results unreliable.\n"
            "  Re-run  npm run train:synthetic-matches  to generate predictions_eligible_all.csv."
        )
    if source == "oof":
        print(
            "  ✓  OOF predictions: each pair was predicted by a fold that excluded it from training."
        )

    pred_df = pd.read_csv(pred_path)

    # ── Load features from pairs.json ─────────────────────────────────────────
    with PAIRS_PATH.open(encoding="utf-8") as fh:
        raw_pairs: list[dict[str, Any]] = json.load(fh)

    feat_rows: list[dict[str, Any]] = []
    for p in raw_pairs:
        row: dict[str, Any] = {
            "startup_id": p["startup_id"],
            "investor_id": p["investor_id"],
            "_pairs_eligible": bool(p.get("eligible_for_model_ranking", True)),
            "_pairs_label": int(p["label"]),
        }
        for feat, val in p["features"].items():
            row[feat] = float(val) if val is not None else 0.0
        feat_rows.append(row)
    feat_df = pd.DataFrame(feat_rows)

    # ── Merge predictions with features ──────────────────────────────────────
    df = pred_df.merge(feat_df, on=["startup_id", "investor_id"], how="left")

    # ── Determine eligibility column ──────────────────────────────────────────
    if "eligible_for_model_ranking" in df.columns:
        elig_col = "eligible_for_model_ranking"
    elif "_pairs_eligible" in df.columns:
        elig_col = "_pairs_eligible"
    else:
        print("  ⚠  No eligibility column found. Treating all pairs as eligible.")
        df["eligible_for_model_ranking"] = True
        elig_col = "eligible_for_model_ranking"

    df_el = df[df[elig_col].astype(bool)].copy().reset_index(drop=True)

    # Ensure true_label column exists (predictions.csv already has it).
    if "true_label" not in df_el.columns and "_pairs_label" in df_el.columns:
        df_el = df_el.rename(columns={"_pairs_label": "true_label"})

    n_total = len(df)
    n_eligible = len(df_el)
    print(f"  Pairs loaded: {n_total} total → {n_eligible} eligible for ranking")

    # Sort deterministically before assigning random scores.
    df_el = df_el.sort_values(["startup_id", "investor_id"]).reset_index(drop=True)
    return df_el, pred_desc


# ── Aggregate across groups ────────────────────────────────────────────────────


def aggregate_strategy(grp_df: "pd.DataFrame") -> dict[str, Any]:
    """Summarise per-group metrics for one strategy (one side: founder or investor)."""
    n = len(grp_df)
    n_with_relevant = int((grp_df["n_relevant"] > 0).sum())
    mask_rel = grp_df["n_relevant"] > 0  # only groups with ≥1 relevant item

    agg: dict[str, Any] = {
        "n_groups": n,
        "n_groups_with_relevant": n_with_relevant,
    }
    for k in K_VALUES:
        agg[f"n_groups_with_{k}_candidates"] = int((grp_df["n_candidates"] >= k).sum())
        for metric in [f"ndcg_at_{k}", f"precision_at_{k}", f"recall_at_{k}", f"ap_at_{k}"]:
            vals = grp_df.loc[mask_rel, metric].dropna()
            agg[f"mean_{metric}"] = round(float(vals.mean()), 4) if len(vals) > 0 else float("nan")

    for col in ["mean_rank_relevant", "mean_rank_label4"]:
        vals = grp_df[col].dropna()
        agg[f"overall_{col}"] = round(float(vals.mean()), 4) if len(vals) > 0 else float("nan")

    return agg


# ── Report generation ─────────────────────────────────────────────────────────


def _fmt(v: "float | Any", decimals: int = 4) -> str:
    """Format float for report; returns '—' for NaN."""
    if isinstance(v, float) and (v != v):  # NaN check
        return "—"
    if isinstance(v, float):
        return f"{v:.{decimals}f}"
    return str(v)


